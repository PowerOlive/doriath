var block170000 []byte

func TestBlockSerialization(t *testing.T) {
	var blk Block
	blk.Deserialize(block170000)
	if bytes.Compare(blk.Serialize(), block170000) != 0 {
		t.Fail()
	}
}

func TestGenMerkle(t *testing.T) {
	var blk Block
	blk.Deserialize(block170000)
	for _, tx := range blk.Bdy {
		claimedProof, pos := blk.GenMerkle(tx.Hash256())
		if !blk.Hdr.CheckMerkle(claimedProof, pos, tx) {
			t.FailNow()
		}
	}
}

